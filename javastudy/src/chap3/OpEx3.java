package chap3;

public class OpEx3 {

	public static void main(String[] args) {
		int x = 10, y = 8;
		double d = (float)x/y;
		System.out.println(d);
		System.out.println("10/8="+(10/8));
		System.out.println("-10/8="+(-10/8));
		System.out.println("10/-8="+(10/-8));
		System.out.println("-10/-8="+(-10/-8));
		System.out.println("10%8="+(10%8));
		System.out.println("-10%8="+(-10%8));
		System.out.println("10%-8="+(10%-8));
		System.out.println("-10%-8="+(-10%-8));
		
	}

}
